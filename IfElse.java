class IfElse {
    public static void main (String args []){
        String verificador = "trudfde";
        if (verificador == "true")
            System.out.println("true");
        else if (verificador == "false")
            System.out.println("false");
        else
            System.out.println("Nenhuma das anteriores");

        char sexo = 'g';
        if (sexo == 'm')
            System.out.println("Masculino");
        else if (sexo == 'f')
            System.out.println();
        else
            System.out.println("não binário");
    }
}